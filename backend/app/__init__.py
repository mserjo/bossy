# Це дозволяє запускати додаток через uvicorn backend.app:app
# Припускаємо, що main.py знаходиться в backend/app/main.py
# і визначає змінну `app = FastAPI(...)`.
try:
    from .main import app as main_app # Використовуємо псевдонім, щоб уникнути конфлікту імен, якщо тут буде інший 'app'
    app = main_app # Присвоюємо 'app' на рівні пакета
    __all__ = ["app"]
except ImportError as e:
    # Це може статися, якщо main.py ще не створено або має помилки імпорту.
    # Логування тут може бути корисним, але потрібен логер, налаштований без залежності від app.
    print(f"УВАГА: Не вдалося імпортувати 'app' з '.main' в backend/app/__init__.py: {e}")
    app = None # type: ignore
    __all__ = []


