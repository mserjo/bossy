"""
Головний пакет програми Kudos (`app`).

Цей файл `__init__.py` позначає директорію `app` як основний пакет Python для
бекенд-додатку Kudos. Він слугує точкою входу для доступу до вихідного коду
програми, що зазвичай знаходиться в підпакеті `src`, та до основного екземпляра
FastAPI програми, визначеного в `main.py`.

Основна структура підпакетів, що очікується всередині `app`:
- `src/`: Містить весь основний вихідний код (API, ядро, конфігурація, моделі тощо).
- `main.py`: Файл, що визначає та конфігурує головний екземпляр FastAPI (`app`).

Цей `__init__.py` також ре-експортує головний екземпляр FastAPI (`app`)
з модуля `main` (тобто, `backend.app.main.app`), щоб його можна було
легко імпортувати та використовувати інструментами запуску ASGI-серверів,
такими як Uvicorn. Наприклад: `uvicorn backend.app:app --reload`.
"""

