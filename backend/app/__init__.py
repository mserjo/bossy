# # Цей файл позначає директорію `app` як основний пакет Python для
# # бекенд-додатку Kudos (Virtus). Він слугує точкою входу для доступу
# # до вихідного коду програми (зазвичай у підпакеті `src`) та до
# # основного екземпляра FastAPI програми, визначеного в `main.py`.
# #
# # Ключовою функцією цього `__init__.py` є ре-експорт головного екземпляра
# # FastAPI (`app`) з модуля `main` (тобто, `backend.app.main.app`).
# # Це дозволяє легко імпортувати та використовувати екземпляр `app`
# # інструментами запуску ASGI-серверів, наприклад: `uvicorn backend.app:app --reload`.

# Ре-експорт головного екземпляра FastAPI з main.py.
# Це стандартна практика, яка дозволяє ASGI-серверам (наприклад, Uvicorn)
# знаходити екземпляр FastAPI за шляхом `package_name:variable_name`
# (в нашому випадку, `backend.app:app`).
# Припускаємо, що файл `main.py` знаходиться в поточній директорії (`backend/app/main.py`)
# і визначає змінну `app = FastAPI(...)`.
try:
    # Використовуємо псевдонім `main_app` при імпорті, щоб уникнути потенційного
    # конфлікту імен, якщо б у цьому файлі `__init__.py` була інша змінна `app`.
    from .main import app as main_app

    # Присвоюємо імпортований екземпляр FastAPI змінній `app` на рівні цього пакету.
    app = main_app

    # `__all__` визначає публічний API цього пакету. Якщо хтось зробить `from backend.app import *`,
    # буде імпортовано лише те, що перелічено в `__all__`.
    __all__ = ["app"]
except ImportError as e:
    # Ця помилка може виникнути, якщо:
    # 1. Файл `main.py` ще не створено.
    # 2. Файл `main.py` містить синтаксичні помилки.
    # 3. Файл `main.py` має проблеми з імпортом власних залежностей (циклічні імпорти, відсутні модулі).
    # Логування тут може бути ускладнене, оскільки система логування, ймовірно,
    # ще не налаштована (вона зазвичай налаштовується в `main.py` або при старті `app`).
    # Тому простий `print` є надійним способом повідомити про проблему під час розробки.
    print(
        f"ПОПЕРЕДЖЕННЯ: Не вдалося імпортувати екземпляр 'app' з '.main' в `backend/app/__init__.py`. "
        f"Причина: {e}. Переконайтеся, що `backend/app/main.py` існує та не містить помилок."
    )
    # Визначаємо `app` як None, щоб імпорт `backend.app.app` не викликав AttributeError,
    # хоча програма, ймовірно, не зможе запуститися коректно.
    app = None # type: ignore
    __all__ = [] # Нічого не експортуємо, якщо `app` не вдалося завантажити.


