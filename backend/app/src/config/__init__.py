# backend/app/src/config/__init__.py
# -*- coding: utf-8 -*-
"""Пакет конфігурації для додатку.

Цей пакет містить модулі, що відповідають за налаштування різних аспектів
програми, таких як:
- Загальні налаштування програми (`settings.py`), включаючи змінні середовища.
- Конфігурація підключення до бази даних (`database.py`).
- Налаштування системи логування (`logging_config.py`).
- Конфігурація підключення до Redis (`redis.py`), якщо використовується.
- Параметри безпеки, включаючи налаштування JWT (`security.py`).

Цей `__init__.py` файл також може ре-експортувати ключові конфігураційні
об'єкти для спрощення їх імпорту в інших частинах додатку.
Наприклад, `from backend.app.src.config import settings, logger`.
"""

# Ре-експорт основних конфігураційних об'єктів для зручного доступу.
# Припускаємо, що `settings.py` визначає об'єкт `app_settings` (або `settings`),
# а `logging_config.py` надає налаштований логер `logger`.

from backend.app.src.config.settings import get_settings
from backend.app.src.config.logging_config import setup_logging

# Отримуємо екземпляр налаштувань, щоб він був доступний як `config.settings`
settings = get_settings()

# Налаштовуємо та отримуємо екземпляр логера
logger = setup_logging()

# Визначаємо, які символи будуть експортовані при використанні `from backend.app.src.config import *`.
# Рекомендується використовувати явні імпорти, але `__all__` контролює поведінку "зірочкового" імпорту.
__all__ = [
    "settings",
    "logger",
    "get_settings", # Також експортуємо фабричну функцію для налаштувань
    "setup_logging",# Також експортуємо функцію налаштування логування
]

logger.debug(
    "Пакет конфігурації `config` ініціалізовано. "
    "Налаштування та логер доступні для використання."
)
