# backend/app/src/core/constants.py
# -*- coding: utf-8 -*-
# # Модуль глобальних констант для програми Kudos (Virtus).
# #
# # Цей модуль визначає різноманітні константи, що використовуються в різних
# # частинах програми. Мета полягає в тому, щоб мати єдине, централізоване
# # джерело для таких значень, що полегшує їх підтримку та забезпечує
# # узгодженість у всьому коді.
# #
# # Важливо: Константи, що залежать від середовища виконання (наприклад, URL-адреси
# # сервісів, секретні ключі, налаштування підключень), повинні знаходитися
# # в модулі `backend.app.src.config.settings.py` та завантажуватися зі змінних середовища.

from backend.app.src.config.logging import get_logger # Імпорт логера

# Отримання логера для цього модуля
logger = get_logger(__name__)


# --- Константи пагінації ---
# Використовуються для налаштування поведінки пагінації в API відповідях.
DEFAULT_PAGE_NUMBER: int = 1  # Номер сторінки за замовчуванням для запитів з пагінацією.
DEFAULT_PAGE_SIZE: int = 20   # Розмір сторінки (кількість елементів на сторінці) за замовчуванням.
MAX_PAGE_SIZE: int = 100      # Максимально дозволений розмір сторінки, щоб уникнути надмірного навантаження.
MIN_PAGE_SIZE: int = 1        # Мінімально дозволений розмір сторінки.

# --- Регулярні вирази ---
# Використовуються для валідації рядкових значень, таких як імена користувачів, паролі тощо.

# Регулярний вираз для перевірки формату імені користувача (username).
# Дозволяє буквено-цифрові символи, а також символи підкреслення (_) та дефіс (-).
# Довжина імені користувача повинна бути від 3 до 20 символів.
USERNAME_REGEX: str = r"^[a-zA-Z0-9_-]{3,20}$"

# Регулярний вираз для перевірки надійності пароля.
# Вимоги до пароля:
# - щонайменше 8 символів завдовжки (максимальна довжина 128 символів для запобігання DoS).
# - щонайменше одна мала літера латинського алфавіту (a-z).
# - щонайменше одна велика літера латинського алфавіту (A-Z).
# - щонайменше одна цифра (0-9).
# - щонайменше один спеціальний символ з набору: @, $, !, %, *, ?, &, _
PASSWORD_REGEX: str = r"^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&_])[A-Za-z\d@$!%*?&_]{8,128}$"

# --- Значення за замовчуванням для моделей даних ---
# Використовуються при створенні нових екземплярів моделей, якщо відповідні поля не задані.
DEFAULT_USERNAME_PREFIX: str = "user_" # Префікс для автоматично генерованих імен користувачів (наприклад, якщо реєстрація через соцмережі без надання username).
# TODO i18n: Translatable string. "Моя група" - Назва групи за замовчуванням при її створенні користувачем.
DEFAULT_GROUP_NAME: str = "Моя група"
DEFAULT_TASK_POINTS: int = 10 # Кількість балів (kudos) за виконання завдання за замовчуванням.
DEFAULT_AVATAR_FILENAME: str = "default_avatar.png" # Ім'я файлу стандартного аватара користувача.

# --- Константи, пов'язані з кешуванням ---
# Використовуються для налаштування параметрів кешування (наприклад, Redis).
CACHE_DEFAULT_TTL_SECONDS: int = 300  # Час життя (Time-To-Live) за замовчуванням для записів у кеші (300 секунд = 5 хвилин).
CACHE_KEY_PREFIX_USER: str = "user_cache:"   # Префікс для ключів кешу, пов'язаних з даними користувачів.
CACHE_KEY_PREFIX_GROUP: str = "group_cache:" # Префікс для ключів кешу, пов'язаних з даними груп.
CACHE_KEY_PREFIX_TASK: str = "task_cache:"   # Префікс для ключів кешу, пов'язаних з даними завдань.

# --- Константи, пов'язані із завданнями (Tasks) / подіями (Events) ---
# Обмеження на довжину текстових полів, кількість учасників тощо.
TASK_MAX_DESCRIPTION_LENGTH: int = 5000 # Максимальна довжина опису завдання.
EVENT_MAX_NAME_LENGTH: int = 255        # Максимальна довжина назви події або заходу.

# --- Константи для управління функціоналом (Feature Flags) ---
# Використовуються для увімкнення/вимкнення певних функцій програми без зміни коду.
# Значення цих констант можуть бути назвами змінних середовища, які потім перевіряються.
FEATURE_NEW_DASHBOARD_ENABLED: str = "KUDOS_FEATURE_NEW_DASHBOARD_ENABLED" # Приклад прапорця для нової панелі інструментів.

# --- Важливе зауваження щодо констант, яких тут НЕМАЄ ---
# Наступні типи значень НЕ ПОВИННІ визначатися як глобальні константи в цьому файлі:
#
# 1.  Конфігурації, що залежать від середовища:
#     -   URL-адреси баз даних, Redis, зовнішніх сервісів.
#     -   Секретні ключі (API ключі, JWT секрети, `SECRET_KEY` Django/FastAPI).
#     -   Імена користувачів/паролі для сервісів.
#     -   Режим DEBUG, список дозволених хостів (ALLOWED_HOSTS).
#     **Місцезнаходження**: `backend.app.src.config.settings.py` (завантажуються з .env файлів або змінних середовища).
#
# 2.  Динамічні значення, що залежать від стану програми або даних:
#     -   Ідентифікатори користувачів, сесій, об'єктів.
#     **Місцезнаходження**: Генеруються або отримуються під час виконання програми.
#
# 3.  Шляхи до файлів та директорій, що можуть змінюватися залежно від розгортання:
#     -   Шляхи до директорій для завантажених файлів, логів, статичних файлів.
#     **Місцезнаходження**: `backend.app.src.config.settings.py` (часто з використанням `pathlib` та базових директорій).
#
# 4.  Специфічні для API шляхи та префікси:
#     -   Наприклад, `/api/v1`, `/auth`.
#     **Місцезнаходження**: `backend.app.src.config.settings.py` (для глобальних префіксів) або безпосередньо
#         при визначенні роутерів FastAPI.
#
# 5.  Назви ролей користувачів або дозволів, якщо вони зберігаються в базі даних:
#     -   "admin", "editor", "viewer".
#     **Місцезнаходження**: Можуть бути визначені як Enum в `core.schemas` або `core.dicts`,
#         або завантажуватися з бази даних. Якщо вони фіксовані і використовуються для логіки
#         в коді (не лише для відображення), їх можна визначити тут або в спеціалізованому модулі ролей.
#
# 6.  Регулярні вирази, що використовуються лише в одному місці (наприклад, для валідації конкретного поля Pydantic моделі):
#     **Місцезнаходження**: Безпосередньо в моделі Pydantic або у валідаторі.
#         Тут слід розміщувати лише ті регулярні вирази, що широко використовуються в різних частинах програми.
#
# Мета цього файлу - надати стабільні, незмінні значення, які є частиною самої логіки програми,
# а не її конфігурації для конкретного середовища.

if __name__ == "__main__":
    # Блок для демонстрації визначених констант.
    # Використовується для швидкої перевірки значень при прямому запуску файлу `python constants.py`.
    logger.info("--- Демонстрація основних констант програми Kudos (Virtus) ---")
    logger.info(f"Номер сторінки за замовчуванням: {DEFAULT_PAGE_NUMBER}")
    logger.info(f"Розмір сторінки за замовчуванням: {DEFAULT_PAGE_SIZE}")
    logger.info(f"Максимальний розмір сторінки: {MAX_PAGE_SIZE}")
    logger.info(f"Регулярний вираз для імені користувача: {USERNAME_REGEX}")
    logger.info(f"Регулярний вираз для пароля: {PASSWORD_REGEX}")
    logger.info(f"Ім'я групи за замовчуванням: {DEFAULT_GROUP_NAME} (TODO: Потребує i18n, якщо використовується для відображення)")
    logger.info(f"Час життя кешу за замовчуванням (секунди): {CACHE_DEFAULT_TTL_SECONDS}")
    logger.info(f"Префікс ключа кешу для користувача: {CACHE_KEY_PREFIX_USER}")
    logger.info(f"Ім'я файлу стандартного аватара: {DEFAULT_AVATAR_FILENAME}")
    logger.info(f"Приклад прапорця функції (Feature Flag): {FEATURE_NEW_DASHBOARD_ENABLED}")
    logger.info("\nПам'ятайте: константи, що залежать від середовища, знаходяться в `config.settings.py`.")
