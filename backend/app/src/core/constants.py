# -*- coding: utf-8 -*-
"""Глобальні константи для додатку.

Цей модуль визначає різноманітні константи, що використовуються в різних
частинах програми. Мета полягає в тому, щоб мати єдине джерело для таких значень,
що полегшує їх підтримку та забезпечує узгодженість.

Константи, що залежать від середовища (наприклад, URL-адреси, секретні ключі),
повинні знаходитися в модулі `backend.app.src.config.settings`.
Цей файл призначений для констант, які є незмінними для всіх середовищ
або визначають поведінку програми за замовчуванням.

Сумісність: Python 3.13, SQLAlchemy v2, Pydantic v2.
"""
import logging # Для демонстраційного блоку if __name__ == "__main__"

from backend.app.src.config.logging import get_logger
logger = get_logger(__name__)

# --- Константи пагінації ---
DEFAULT_PAGE_NUMBER: int = 1  # Номер сторінки за замовчуванням для запитів з пагінацією.
DEFAULT_PAGE_SIZE: int = 20   # Розмір сторінки (кількість елементів на сторінці) за замовчуванням.
MAX_PAGE_SIZE: int = 100      # Максимально дозволений розмір сторінки для запитів.
MIN_PAGE_SIZE: int = 1        # Мінімально дозволений розмір сторінки.

# --- Регулярні вирази ---
# Регулярний вираз для перевірки формату імені користувача (username).
# Дозволяє латинські літери (великі та малі), цифри, символи підкреслення (_) та дефіси (-).
# Довжина від 3 до 30 символів.
USERNAME_REGEX: str = r"^[a-zA-Z0-9_-]{3,30}$"

# Регулярний вираз для перевірки надійності пароля (приклад).
# Фактична логіка валідації пароля знаходиться у `backend.app.src.utils.validators.is_strong_password`.
# Цей регулярний вираз наведено для інформаційних цілей або для використання у фронтенді.
# Вимоги:
# - щонайменше 8 символів завдовжки (максимум 128 символів для практичності);
# - щонайменше одна мала літера (a-z);
# - щонайменше одна велика літера (A-Z);
# - щонайменше одна цифра (0-9);
# - щонайменше один спеціальний символ (з набору, визначеного в utils.validators.SPECIAL_CHARACTERS_REGEX).
PASSWORD_REGEX_EXAMPLE: str = (
    r"^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*()_+=\-\[\]{};':\"\\|,.<>\/?~`])[A-Za-z\d!@#$%^&*()_+=\-\[\]{};':\"\\|,.<>\/?~`]{8,128}$"
)

# --- Значення за замовчуванням для моделей даних ---
DEFAULT_USERNAME_PREFIX: str = "user_" # Префікс для автоматично генерованих імен користувачів (якщо така логіка є).
DEFAULT_GROUP_NAME: str = "Моя група" # TODO i18n: Назва групи за замовчуванням при її створенні. Потребує перекладу.
DEFAULT_TASK_POINTS: int = 10 # Кількість балів, що нараховуються за виконання завдання за замовчуванням.
DEFAULT_AVATAR_FILENAME: str = "default_avatar.png" # Ім'я файлу стандартного аватара користувача.

# --- Константи, пов'язані з кешуванням ---
CACHE_DEFAULT_TTL_SECONDS: int = 60 * 5  # Час життя (Time To Live) за замовчуванням для записів у кеші (5 хвилин).
CACHE_KEY_PREFIX_USER_DETAILS: str = "user_details:"   # Префікс для ключів кешу, пов'язаних з детальною інформацією про користувачів.
CACHE_KEY_PREFIX_GROUP_INFO: str = "group_info:" # Префікс для ключів кешу, пов'язаних з інформацією про групи.
CACHE_KEY_PREFIX_TASK_DATA: str = "task_data:"   # Префікс для ключів кешу, пов'язаних із завданнями.

# --- Константи, пов'язані із завданнями, подіями або іншими сутностями ---
TASK_MAX_DESCRIPTION_LENGTH: int = 5000 # Максимально дозволена довжина опису для завдання.
EVENT_MAX_NAME_LENGTH: int = 255        # Максимально дозволена довжина назви для події.

# Статуси завершення завдань
TASK_COMPLETION_STATUS_APPROVED = "APPROVED"
TASK_COMPLETION_STATUS_PENDING = "PENDING"
TASK_COMPLETION_STATUS_REJECTED = "REJECTED"
# Додайте інші статуси, якщо потрібно


# --- Інші загальні константи ---
# Приклад константи для управління функціоналом (feature flag).
# Може використовуватися для увімкнення/вимкнення нових або експериментальних функцій.
# Значення може читатися з конфігурації середовища або бути жорстко заданим.
FEATURE_ADVANCED_ANALYTICS_ENABLED: bool = False # Приклад: розширена аналітика вимкнена за замовчуванням.

# --- Константи ролей ---
ADMIN_ROLE_CODE: str = "ADMIN"  # Код ролі адміністратора системи або групи

# Коментар щодо констант, які НЕ повинні бути тут:
# Важливо пам'ятати, що константи, які залежать від середовища розгортання
# (наприклад, URL-адреси зовнішніх сервісів, секретні ключі, API-ключі),
# повинні визначатися в `config.settings.py` та завантажуватися зі змінних середовища або .env файлів.
# Також, значення, що часто змінюються або є частиною бізнес-логіки,
# можуть бути краще представлені як налаштування в базі даних або в `settings.py`.
# Ролі користувачів (ROLE_SUPERUSER, ROLE_ADMIN) краще визначати як Enum
# або отримувати з бази даних/іншого джерела конфігурації прав доступу.

if __name__ == "__main__":
    # Блок для демонстрації визначених констант.
    # Використовується для швидкої перевірки значень при прямому запуску файлу.
    # Логер вже налаштований через імпорт `from backend.app.src.config import logger`.
    # Якщо цей файл запускається абсолютно окремо, може знадобитися локальне налаштування логування.
    if not logging.getLogger(logger.name).hasHandlers(): # Перевіряємо, чи наш конкретний логер має обробники
        logging.basicConfig(level=logging.INFO) # Налаштовуємо базове логування, якщо потрібно

    logger.info("--- Демонстрація основних констант додатку ---")
    logger.info("Розмір сторінки за замовчуванням: %d", DEFAULT_PAGE_SIZE)
    logger.info("Максимальний розмір сторінки: %d", MAX_PAGE_SIZE)
    logger.info("Регулярний вираз для імені користувача: %s", USERNAME_REGEX)
    logger.info("Приклад регулярного виразу для пароля: %s", PASSWORD_REGEX_EXAMPLE)
    logger.info("Час життя кешу за замовчуванням (секунди): %d", CACHE_DEFAULT_TTL_SECONDS)
    logger.info("Префікс ключа кешу для деталей користувача: %s", CACHE_KEY_PREFIX_USER_DETAILS)
    logger.info("Ім'я файлу стандартного аватара: %s", DEFAULT_AVATAR_FILENAME)
    logger.info("Прапорець функції розширеної аналітики: %s", FEATURE_ADVANCED_ANALYTICS_ENABLED)
    logger.info("Код ролі адміністратора: %s", ADMIN_ROLE_CODE)
    logger.info("Назва групи за замовчуванням: %s (TODO i18n: '%s')", DEFAULT_GROUP_NAME, DEFAULT_GROUP_NAME)

    logger.info("\n--- Константи статусів завершення завдань ---")
    logger.info("Статус 'Затверджено': %s", TASK_COMPLETION_STATUS_APPROVED)
    logger.info("Статус 'Очікує': %s", TASK_COMPLETION_STATUS_PENDING)
    logger.info("Статус 'Відхилено': %s", TASK_COMPLETION_STATUS_REJECTED)
