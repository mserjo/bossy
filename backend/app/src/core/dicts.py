# -*- coding: utf-8 -*-
# # Модуль системних переліків (Enums) для програми Kudos (Virtus).
# #
# # Цей модуль визначає набори фіксованих значень (переліки), які використовуються
# # для представлення стандартизованих станів, типів, ролей та інших категорій
# # в межах бізнес-логіки програми. Використання `enum.Enum` допомагає забезпечити
# # узгодженість даних, покращує читабельність коду та зменшує ймовірність помилок.
# # Ці переліки є частиною ядра програми і не призначені для динамічного
# # управління через адміністративний інтерфейс (на відміну від моделей-довідників,
# # що зберігаються в базі даних та керуються через API).

from enum import Enum # Базовий клас для створення переліків
from backend.app.src.config.logging import get_logger # Імпорт логера

# Отримання логера для цього модуля
logger = get_logger(__name__)


class SortOrder(str, Enum):
    """Визначає порядок сортування для результатів запитів та списків."""
    ASC = "asc"         # Сортування за зростанням (A-Z, 0-9).
    DESC = "desc"       # Сортування за спаданням (Z-A, 9-0).

class UserState(str, Enum):
    """Представляє можливі стани облікового запису користувача в системі."""
    PENDING_VERIFICATION = "pending_verification" # Обліковий запис створено, але очікує на підтвердження (наприклад, через email).
    ACTIVE = "active"                             # Обліковий запис активний і може використовуватися для входу та взаємодії.
    INACTIVE = "inactive"                         # Обліковий запис деактивовано користувачем або автоматично за тривалою неактивністю.
    SUSPENDED = "suspended"                       # Обліковий запис тимчасово призупинено адміністратором системи (може бути відновлено).
    BANNED = "banned"                             # Обліковий запис остаточно заблоковано адміністратором системи (без можливості відновлення).
    DELETED = "deleted"                           # Обліковий запис позначено для видалення (м'яке видалення, дані можуть бути анонімізовані або видалені пізніше).

class SystemUserRole(str, Enum):
    """Визначає глобальні системні ролі користувачів, що впливають на доступ до всієї системи."""
    SUPERUSER = "superuser" # Суперкористувач, має повний необмежений доступ до всіх функцій та даних системи.
    USER = "user"           # Звичайний користувач системи зі стандартними правами.
    BOT = "bot"             # Системний бот для виконання автоматизованих завдань (наприклад, розсилки, фонові обчислення).
    # Примітка: Ролі, специфічні для контексту (наприклад, "адміністратор групи"), визначаються окремо (див. `GroupRole`).

class UserType(str, Enum):
    """Визначає типи користувачів на основі їх реєстрації або системного призначення, згідно з тех. завданням."""
    # Відповідно до пункту "тип користувача - суперюзер, адмін, користувач, бот" з технічного завдання.
    # Тут "адмін" інтерпретується як тип користувача, що має потенціал бути адміністратором (наприклад, групи),
    # але його конкретні адміністративні права в контексті групи визначаються через `GroupRole`.
    SUPERUSER = "superuser"     # Тип для суперкористувачів системи.
    ADMIN_USER = "admin_user"   # Тип користувача, який може виконувати адміністративні функції (наприклад, керувати групами).
    REGULAR_USER = "regular_user" # Тип для звичайних зареєстрованих користувачів.
    BOT_USER = "bot_user"         # Тип для системних ботів або автоматизованих агентів.

class GroupRole(str, Enum):
    """Визначає ролі користувачів всередині конкретної групи або команди."""
    OWNER = "owner"       # Власник групи (зазвичай той, хто створив групу, з максимальними правами в межах групи).
    ADMIN = "admin"       # Адміністратор групи, може керувати учасниками, завданнями та налаштуваннями групи.
    MEMBER = "member"     # Звичайний учасник групи з базовими правами (наприклад, виконувати завдання, переглядати інформацію).
    # GUEST = "guest"     # Опціонально: гостьова роль з обмеженими правами (наприклад, тільки перегляд).

class GroupType(str, Enum):
    """Визначає типи груп, відповідно до класифікації в технічному завданні."""
    FAMILY = "family"           # Тип групи "Сім'я" (для сімейних завдань та винагород).
    DEPARTMENT = "department"   # Тип групи "Відділ" (для робочих команд або підрозділів).
    ORGANIZATION = "organization" # Тип групи "Організація" (для більших структур або компаній).

class TaskStatus(str, Enum):
    """Представляє можливі статуси життєвого циклу завдання, події або штрафу."""
    OPEN = "open"                             # Завдання відкрите, доступне для взяття в роботу або очікує на виконання.
    IN_PROGRESS = "in_progress"               # Завдання взято в роботу та активно виконується.
    PENDING_REVIEW = "pending_review"         # Виконання завдання завершено користувачем та очікує на перевірку/затвердження.
    COMPLETED = "completed"                   # Завдання успішно виконано та затверджено (якщо потрібне затвердження).
    REJECTED = "rejected"                     # Виконання завдання було відхилено після перевірки.
    CANCELLED = "cancelled"                   # Завдання було скасовано до його завершення (наприклад, стало неактуальним).
    ON_HOLD = "on_hold"                       # Виконання завдання тимчасово призупинено.
    EXPIRED = "expired"                       # Термін виконання завдання минув до його завершення або взяття в роботу.

class TaskType(str, Enum):
    """Визначає типи завдань, відповідно до класифікації в технічному завданні."""
    REGULAR_TASK = "regular_task"   # "Звичайне завдання": стандартне завдання з балами.
    COMPLEX_TASK = "complex_task"   # "Складне завдання": може мати підзавдання або особливі умови.
    EVENT = "event"                 # "Подія": завдання, пов'язане з конкретною подією або датою.
    FINE = "fine"                   # "Штраф": завдання, невиконання якого призводить до штрафу, або саме по собі є штрафом.

class EventFrequency(str, Enum):
    """Визначає частоту повторення для завдань або подій, що регулярно повторюються."""
    ONCE = "once"           # Одноразова подія/завдання, не повторюється.
    DAILY = "daily"         # Повторюється щоденно.
    WEEKLY = "weekly"       # Повторюється щотижня.
    MONTHLY = "monthly"     # Повторюється щомісяця.
    YEARLY = "yearly"       # Повторюється щорічно.
    # CUSTOM = "custom"     # Для більш складних шаблонів повторення (потребуватиме додаткових полів для налаштування).

class BonusType(str, Enum):
    """Визначає типи бонусних операцій, відповідно до технічного завдання."""
    REWARD = "reward"   # Нарахування бонусів (позитивна операція, наприклад, за виконане завдання).
    PENALTY = "penalty" # Списання бонусів/штраф (негативна операція, наприклад, за невиконане завдання-штраф).

class TransactionType(str, Enum):
    """Визначає типи фінансових транзакцій або операцій з балами на рахунку користувача."""
    CREDIT = "credit"   # Нарахування коштів/бонусів на рахунок (збільшення балансу).
    DEBIT = "debit"     # Списання коштів/бонусів з рахунку (зменшення балансу).
    REFUND = "refund"   # Повернення коштів/бонусів на рахунок.
    ADJUSTMENT_INCREASE = "adjustment_increase" # Ручне коригування балансу (збільшення) адміністратором.
    ADJUSTMENT_DECREASE = "adjustment_decrease" # Ручне коригування балансу (зменшення) адміністратором.

class NotificationType(str, Enum):
    """Визначає різні типи системних сповіщень для користувачів."""
    GENERAL_INFO = "general_info"                   # Загальна інформація або оголошення.
    TASK_ASSIGNED = "task_assigned"                 # Користувачу призначено нове завдання.
    TASK_COMPLETED_BY_USER = "task_completed_by_user" # Користувач позначив завдання як виконане (очікує перевірки).
    TASK_VERIFIED_BY_ADMIN = "task_verified_by_admin" # Адміністратор затвердив або відхилив виконання завдання.
    TASK_REMINDER = "task_reminder"                 # Нагадування про наближення терміну виконання завдання.
    BONUS_AWARDED = "bonus_awarded"                 # Користувачу нараховано бонуси.
    PENALTY_APPLIED = "penalty_applied"             # До користувача застосовано штраф (списано бонуси).
    REWARD_REDEEMED = "reward_redeemed"             # Користувач успішно отримав (витратив бали на) нагороду.
    GROUP_INVITATION = "group_invitation"           # Користувача запрошено приєднатися до групи.
    NEW_MEMBER_JOINED_GROUP = "new_member_joined_group" # Новий учасник приєднався до групи користувача.
    ACCOUNT_BALANCE_UPDATE = "account_balance_update" # Інформація про зміну балансу рахунку.
    SYSTEM_ANNOUNCEMENT = "system_announcement"       # Важливе загальносистемне оголошення.

class FileType(str, Enum):
    """Категоризує типи файлів, що завантажуються або використовуються в системі."""
    AVATAR = "avatar"                 # Аватар користувача.
    GROUP_ICON = "group_icon"         # Іконка/логотип групи.
    REWARD_ICON = "reward_icon"       # Іконка для нагороди.
    BADGE_ICON = "badge_icon"         # Іконка для бейджа або досягнення.
    TASK_ATTACHMENT = "task_attachment" # Файл, прикріплений до завдання (наприклад, інструкція, результат).
    GENERAL_DOCUMENT = "general_document" # Загальний документ (наприклад, правила, політики).
    IMAGE = "image"                   # Зображення загального призначення.
    VIDEO = "video"                   # Відеофайл.
    AUDIO = "audio"                   # Аудіофайл.
    OTHER = "other"                   # Інший тип файлу, що не підпадає під перелічені категорії.

class TimePeriod(str, Enum):
    """Представляє загальні періоди часу для використання у звітах, фільтрації даних або статистиці."""
    TODAY = "today"                 # Сьогоднішній день.
    YESTERDAY = "yesterday"         # Вчорашній день.
    LAST_7_DAYS = "last_7_days"     # Останні 7 днів (включаючи сьогодні).
    LAST_30_DAYS = "last_30_days"   # Останні 30 днів (включаючи сьогодні).
    CURRENT_MONTH = "current_month" # Поточний календарний місяць.
    PREVIOUS_MONTH = "previous_month" # Попередній календарний місяць.
    CURRENT_YEAR = "current_year"   # Поточний календарний рік.
    ALL_TIME = "all_time"           # За весь період існування даних.


