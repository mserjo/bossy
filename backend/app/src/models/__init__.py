# # Ініціалізаційний файл для пакету моделей SQLAlchemy (`models`) програми Kudos (Virtus).
# #
# # Цей пакет є центральним сховищем для всіх моделей даних SQLAlchemy,
# # що відображають структуру бази даних програми. Він організований наступним чином:
# #   - `base.py`: Містить декларативний базовий клас (`Base`) для всіх моделей,
# #     а також спільні базові моделі, такі як `BaseModel` (з полями `id`, `created_at`, `updated_at`)
# #     та `BaseMainModel` (розширює `BaseModel` полями `name`, `description` тощо).
# #   - `mixins.py`: Містить SQLAlchemy міксини для додавання спільної функціональності
# #     до моделей (наприклад, `SoftDeleteMixin`).
# #   - Тематичні підпакети (наприклад, `auth`, `groups`, `tasks`): Кожен містить
# #     моделі, що належать до відповідної предметної області.
# #
# # Цей `__init__.py` ре-експортує ключові базові класи (`Base`, `BaseMainModel`)
# # та всі модулі/підпакети моделей для зручного доступу з інших частин програми.
# # Конкретні класи моделей (наприклад, `User`, `Task`) зазвичай експортуються
# # з `__init__.py` файлів їхніх відповідних підпакетів.

