"""Модуль кастомних функцій валідації даних.

Цей модуль надає функції для перевірки даних на відповідність певним критеріям
або форматам. Ці валідатори можуть використовуватися в моделях Pydantic,
сервісному шарі або інших частинах додатку, де потрібна специфічна
перевірка даних перед їх обробкою або збереженням. Наприклад, перевірка
надійності пароля або формату номера телефону.
"""
import logging  # Для локального використання в __main__
import re
from typing import Optional

from backend.app.src.config.logging import setup_logging
logger = setup_logging()

# Визначення набору спеціальних символів для перевірки пароля.
# Екранування символів, що мають спеціальне значення в регулярних виразах (наприклад, `\` , `[`).
SPECIAL_CHARACTERS_REGEX = r"[!@#$%^&*()_+=\-\[\]{};':\"\\|,.<>\/?~`]"


def is_strong_password(password: str, min_length: int = 8) -> bool:
    """Перевіряє, чи відповідає пароль загальним критеріям надійності.

    Критерії надійності пароля:
        - Мінімальна довжина (за замовчуванням 8 символів).
        - Наявність принаймні однієї великої літери (A-Z).
        - Наявність принаймні однієї малої літери (a-z).
        - Наявність принаймні однієї цифри (0-9).
        - Наявність принаймні одного спеціального символу (з набору `SPECIAL_CHARACTERS_REGEX`).

    Args:
        password: Рядок пароля для перевірки.
        min_length: Мінімальна необхідна довжина пароля. За замовчуванням 8.

    Returns:
        `True`, якщо пароль відповідає всім критеріям надійності, `False` в іншому випадку.
    """
    if not password:
        logger.debug("Перевірка пароля не вдалася: пароль порожній.")
        return False

    if len(password) < min_length:
        logger.debug(
            "Перевірка пароля не вдалася: довжина пароля %d менша за мінімальну (%d).",
            len(password), min_length
        )
        return False
    if not re.search(r"[A-Z]", password):
        logger.debug("Перевірка пароля не вдалася: відсутня принаймні одна велика літера.")
        return False
    if not re.search(r"[a-z]", password):
        logger.debug("Перевірка пароля не вдалася: відсутня принаймні одна мала літера.")
        return False
    if not re.search(r"[0-9]", password):
        logger.debug("Перевірка пароля не вдалася: відсутня принаймні одна цифра.")
        return False
    if not re.search(SPECIAL_CHARACTERS_REGEX, password):
        logger.debug("Перевірка пароля не вдалася: відсутній принаймні один спеціальний символ.")
        return False

    logger.debug("Перевірка пароля успішна: пароль відповідає всім критеріям надійності.")
    return True


def is_valid_phone_number(phone_number: str, region: Optional[str] = None) -> bool:
    """Перевіряє номер телефону на відповідність базовому формату.

    УВАГА: Це дуже базова реалізація валідації номера телефону.
    Вона перевіряє лише загальну структуру та наявність дозволених символів.
    Для надійної валідації міжнародних номерів телефонів у продуктивному середовищі
    наполегливо рекомендується використовувати спеціалізовані бібліотеки,
    такі як `phonenumbers` (порт бібліотеки Google libphonenumber).

    Args:
        phone_number: Рядок номера телефону для перевірки.
        region: Необов'язково. Код регіону (наприклад, "UA", "GB") для більш
                конкретної валідації при використанні спеціалізованої бібліотеки.
                У цій базовій версії параметр `region` не використовується.

    Returns:
        `True`, якщо формат номера телефону виглядає загалом дійсним, `False` в іншому випадку.
    """
    if not phone_number:
        logger.debug("Перевірка номера телефону не вдалася: номер телефону порожній.")
        return False

    # Видаляємо потенційні пробіли на початку та в кінці
    cleaned_phone_number = phone_number.strip()
    if not cleaned_phone_number: # Якщо після очищення рядок порожній
        logger.debug("Перевірка номера телефону не вдалася: номер телефону порожній після очищення.")
        return False

    # Підрахунок лише цифр
    num_digits = sum(c.isdigit() for c in cleaned_phone_number)
    # Дуже загальне правило; реальні номери можуть значно варіюватися.
    # Наприклад, короткі номери екстрених служб.
    if not (3 <= num_digits <= 15): # Розширено діапазон для коротких номерів
        logger.debug(
            "Перевірка номера телефону '%s' не вдалася: містить %d цифр, очікується в діапазоні 3-15.",
            cleaned_phone_number, num_digits
        )
        return False

    # Дозволені символи: цифри, пробіли, дефіси, круглі дужки, плюс на початку.
    # Цей шаблон є досить гнучким.
    allowed_chars_pattern = re.compile(r"^\+?[0-9\s\-\(\)]*$")
    if not allowed_chars_pattern.match(cleaned_phone_number):
        logger.debug(
            "Перевірка номера телефону '%s' не вдалася: містить недійсні символи.",
            cleaned_phone_number
        )
        return False

    # Прості перевірки: чи починається з '+' або цифри.
    # Не перевіряємо закінчення на цифру, бо форматування може включати дужки.
    if not (cleaned_phone_number.startswith('+') or cleaned_phone_number[0].isdigit()):
        logger.debug(
            "Перевірка номера телефону '%s' не вдалася: не починається з '+' або цифри.",
            cleaned_phone_number
        )
        return False

    logger.debug("Номер телефону '%s' пройшов базову перевірку формату.", cleaned_phone_number)
    return True


if __name__ == "__main__":
    # Налаштування базового логування для демонстрації, якщо ще не налаштовано.
    if not logging.getLogger().hasHandlers(): # Перевіряємо, чи є вже обробники у кореневого логера
        logging.basicConfig(
            level=logging.DEBUG,
            format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
        )

    # Використовуємо логер модуля для повідомлень в __main__
    main_logger = logging.getLogger(__name__) # Отримуємо логер для __main__ контексту
    main_logger.info("--- Демонстрація Утиліт Валідації ---")

    main_logger.info("\n--- Тести Надійності Пароля ---")
    passwords_to_test = {
        "Короткий1!": False,
        "безвеликої1!": False,
        "БЕЗМАЛОЇ1!": False,
        "БезЦифри!Аа": False,
        "БезСпецСимволу1Аа": False,
        "ВаліднийПароль123!": True,
        "Інший_Хороший-Пароль_123": True,
        "Слабкий": False,
        "МіцнийПас1$": True,
        "": False, # Тест порожнього пароля
    }
    for pw, expected in passwords_to_test.items():
        is_valid = is_strong_password(pw)
        main_logger.info("Пароль '%s': Надійний? %s (Очікується: %s)", pw, is_valid, expected)
        assert is_valid == expected, f"Тест надійності пароля для '{pw}' не пройдено."

    main_logger.info("\n--- Базові Тести Валідації Номера Телефону ---")
    phones_to_test = {
        "+380501234567": True,
        "050-123-45-67": True,
        "(097) 123 4567": True,
        "1234567": True,
        "123": True, # Мінімальна кількість цифр (наприклад, екстрений номер)
        "12": False, # Занадто мало цифр
        "1234567890123456": False, # Занадто багато цифр
        "0441234567": True,
        "050-123-АБВГ": False, # Недійсні символи
        "+1-555-123-4567 ext 9": False, # Непідтримувані розширення в базовій версії
        "невірний номер": False,
        "": False,
        " ": False, # Тест порожнього рядка після strip
        "+": False, # Тільки плюс не є дійсним номером
        "123-": True, # Може бути дійсним, якщо далі йдуть цифри, але тут наш валідатор пропустить
                      # Однак, якщо це кінець рядка, це може бути недійсним у реальному житті.
                      # Поточний валідатор може потребувати доопрацювання для таких випадків.
        "(044)1234567": True,
    }
    for phone, expected in phones_to_test.items():
        is_valid = is_valid_phone_number(phone)
        main_logger.info("Телефон '%s': Дійсний? %s (Очікується: %s)", phone, is_valid, expected)
        assert is_valid == expected, f"Тест валідації номера телефону для '{phone}' не пройдено."

    main_logger.info(
        "\nПримітка: Перевірка номера телефону є ДУЖЕ БАЗОВОЮ. "
        "Для надійного використання в продуктивному середовищі "
        "рекомендується інтегрувати спеціалізовану бібліотеку (наприклад, 'phonenumbers')."
    )
