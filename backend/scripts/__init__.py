"""
Модуль скриптів для автоматизації завдань та управління додатком.

Цей пакет містить різноманітні командні скрипти, які допомагають
у розробці, розгортанні, тестуванні та обслуговуванні додатка.
Скрипти можуть включати завдання, такі як створення суперкористувача,
запуск міграцій бази даних, виконання тестів, генерація документації API тощо.

Для використання конкретного скрипта, зазвичай, його запускають з командного рядка,
перебуваючи в кореневій директорії проекту або в директорії `backend`.
Наприклад: `python -m backend.scripts.create_superuser`
"""

import logging

# Налаштування базового логера для скриптів, якщо вони не імпортують конфігурацію логування додатка.
# Це допоможе бачити лог-повідомлення від скриптів, якщо вони викликаються окремо.
# Якщо скрипти інтегруються з логером додатка, це налаштування може бути менш критичним.
scripts_logger = logging.getLogger(__name__)
if not scripts_logger.hasHandlers():
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(name)s - %(levelname)s - %(message)s')

scripts_logger.info("Модуль скриптів 'backend.scripts' ініціалізовано.") # i18n
# Це повідомлення буде виведено, якщо __init__.py виконується або імпортується таким чином,
# що логер спрацьовує, наприклад, при `python -m backend.scripts` (якщо б це було можливо).
# В основному, це для позначення, що пакет розпізнано.

